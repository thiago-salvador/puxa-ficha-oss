  ementa,
  signatario,
  autoridade_papel,
  fonte_primaria_url,
  fonte_primaria_titulo,
  identificador_fonte,
  metadata
)
SELECT
  candidato_id,
  historico_politico_id,
  tipo_relacao,
  esfera,
  uf_norma,
  municipio_norma,
  tipo_norma,
  numero,
  ano,
  data_norma::date,
  ementa,
  signatario,
  autoridade_papel,
  fonte_primaria_url,
  fonte_primaria_titulo,
  identificador_fonte,
  metadata
FROM target
WHERE historico_politico_id IS NOT NULL
  AND NOT EXISTS (
    SELECT 1
    FROM public.legislacao_mandato_executivo lme
    WHERE lme.candidato_id = target.candidato_id
      AND lme.identificador_fonte = target.identificador_fonte
  );

DO $$
DECLARE
  rows_inserted integer;
BEGIN
  SELECT count(*)
    INTO rows_inserted
  FROM public.legislacao_mandato_executivo lme
  JOIN public.candidatos c ON c.id = lme.candidato_id
  WHERE c.slug = 'eduardo-braide'
    AND lme.identificador_fonte LIKE 'SAOLUIS-DOM:%';

  IF rows_inserted < 5 THEN
    RAISE EXCEPTION 'eduardo-braide legislacao executiva municipal incompleta: %', rows_inserted;
  END IF;
END $$;
