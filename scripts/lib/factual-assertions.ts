export const CANDIDATE_ASSERTIONS: CandidateAssertion[] = RAW_CANDIDATE_ASSERTIONS.map((item) => {
  const { expected_cargos: rawCargos, ...rest } = item
  const expected_cargos = normalizeExpectedCargos(rawCargos)
  return {
    ...rest,
    expected: sanitizeExpectedSnapshot(rest.expected),
    verifiedAt: item.verifiedAt ?? inferVerifiedAt(item.source),
    ...(expected_cargos !== undefined ? { expected_cargos } : {}),
  }
})

export const ASSERTIONS_MAP = new Map(
  CANDIDATE_ASSERTIONS.map((item) => [item.slug, item])
)

export function getAssertionSlugsForCohort(cohort: AssertionCohort): string[] {
  const normalizedCohort =
    cohort === "alto-trafego-editorial" ? "alto-trafego" : cohort

  return CANDIDATE_ASSERTIONS.filter((item) => item.cohorts.includes(normalizedCohort)).map(
    (item) => item.slug
  )
}
