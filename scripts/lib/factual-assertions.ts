import { sanitizeNullablePtBrText } from "../../src/lib/ptbr-text"
import type { CandidatePublicSnapshot } from "./audit-types"

export type AssertionCohort =
  | "presidenciaveis"
  | "governadores-prioritarios"
  | "governadores"
  // "alto-trafego" e mantido por compatibilidade de CLI e artefatos.
  // Conceitualmente esta coorte representa trafego editorial estimado,
  // nao analytics reais do site.
  | "alto-trafego"
  | "alto-trafego-editorial"

export type AssertionConfidence = "curated" | "mirrored"

export interface CandidateAssertion {
  slug: string
  source: string
  verifiedAt: string
  confidence: AssertionConfidence
  cohorts: AssertionCohort[]
  expected: Partial<CandidatePublicSnapshot>
  /**
   * Curadoria (confidence curated): cargos canônicos que devem existir no historico_politico,
   * alinhados a `canonicalCargo()` / coluna cargo_canonico. Ordem irrelevante.
   */
  expected_cargos?: string[]
}

interface CandidateAssertionInput extends Omit<CandidateAssertion, "verifiedAt"> {
  verifiedAt?: string
}

function normalizeExpectedCargos(cargos: string[] | undefined): string[] | undefined {
  if (!cargos?.length) return undefined
  return [...new Set(cargos.map((c) => c.trim()).filter(Boolean))].sort((a, b) =>
    a.localeCompare(b, "pt-BR")
  )
}

const DEFAULT_VERIFIED_AT = "2026-04-01"

function inferVerifiedAt(source: string, fallback = DEFAULT_VERIFIED_AT): string {
  const match = source.match(/\b(20\d{2}-\d{2}-\d{2})\b/)
  return match?.[1] ?? fallback
}

function sanitizeExpectedSnapshot(snapshot: Partial<CandidatePublicSnapshot>): Partial<CandidatePublicSnapshot> {
  return {
    ...snapshot,
    partido_atual: sanitizeNullablePtBrText(snapshot.partido_atual),
    cargo_atual: sanitizeNullablePtBrText(snapshot.cargo_atual),
    cargo_disputado: sanitizeNullablePtBrText(snapshot.cargo_disputado),
  }
}

